# 求模运算
'''
有时计算结果可能会溢出，此时往往需要对结果取余。
如果有a % m = c % m 和 b % m = d % m, 那么有以下模运算成立。
    (a + b) % m = (c + d) % m
    (a - b) % m = (c - d) % m
    (a × b) % m = (c × d) % m
需要注意的是没有除法运算，另外由于最终结果可能溢出，故需要使用更大范围的类型来保存求模之前的结果。另外若a是负数时往往需要改写为 a % m + m, 这样就保证结果在[0, m - 1]范围内了。
'''


