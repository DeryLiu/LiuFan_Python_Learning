# 素数基础理论
'''
素数：恰好有两个约数的整数，一个是1，另一个则是它自己，比如整数3和5就是素数。
素数的基本算法有素性测试、埃氏筛法和整数分解。

1.素性测试：
如果d是n的约数，则易知 n=d⋅(n/d), 因此 n/d也是n的约数，且这两个约数中的较小者 min(d,n/d)<=√n . 因此我们只需要对前 √n 个数进行处理。

2.埃氏筛法:
素性测试针对的是单个整数，如果需要枚举整数n以内的素数就需要埃氏筛法了。核心思想是枚举从小到大的素数并将素数的整数倍依次从原整数数组中删除，余下的即为全部素数。

3.区间筛法:
求区间[a, b)内有多少素数？
埃氏筛法得到的是[1, n)内的素数，求区间素数时不太容易直接求解，我们采取以退为进的思路先用埃氏筛法求得[1, b)内的素数，然后截取为[a, b)即可。
'''


